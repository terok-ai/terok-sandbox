async def _on_startup(app: web.Application) -> None:
    """Create the shared HTTP client session and start background refresh."""
    # keepalive_timeout=30 ensures we retire connections before upstream servers
    # close them (~60-90s typical for Anthropic/OpenAI), preventing stale-pool 502s.
    connector = TCPConnector(enable_cleanup_closed=True, keepalive_timeout=30)
    app[_KEY_CLIENT] = ClientSession(connector=connector)
    app[_KEY_REFRESH_TASK] = asyncio.create_task(_refresh_loop(app))


async def _on_cleanup(app: web.Application) -> None:
    """Close client session, cancel background tasks, and close token DB."""
    task = app.get(_KEY_REFRESH_TASK)
    if task is not None:
        task.cancel()
        # contextlib.suppress sidesteps Sonar S7497 — the CancelledError
        # here is the *child*'s acknowledgement of our explicit cancel(),
        # not the cleanup task's own cancellation, so it must not propagate.
        with contextlib.suppress(asyncio.CancelledError):
            await task
    await app[_KEY_CLIENT].close()
    app[_KEY_TOKEN_DB].close()
    audit = app.get(_KEY_AUDIT)
    if audit is not None:
        await audit.close()


async def _handle_health(_request: web.Request) -> web.Response:
    """Return 200 — unauthenticated readiness probe for lifecycle management."""
    return web.json_response({"status": "ok"})


def _build_app(db_path: str, routes_path: str, audit_path: Path | None = None) -> web.Application:
    """Construct the aiohttp application.

    *audit_path* — when supplied, every credential-bearing request lands
    one JSONL line at this path.  ``None`` disables auditing (used by
    smoke tests that don't care about the side-channel); production
    callers point at [`credential_audit_log_path`][terok_sandbox.vault.daemon.audit.credential_audit_log_path].
    """
    app = web.Application(client_max_size=_MAX_REQUEST_BODY_SIZE)
    app[_KEY_ROUTES] = _RouteTable(routes_path)
    app[_KEY_TOKEN_DB] = _TokenDB(db_path)
    # Default refresh-lock dir for standalone callers; VaultProxy.start
    # overrides this with the dir resolved from its injected runtime_dir.
    app[_KEY_LOCK_DIR] = _ambient_lock_dir()
    if audit_path is not None:
        app[_KEY_AUDIT] = AuditWriter(audit_path)
    app.on_startup.append(_on_startup)
    app.on_cleanup.append(_on_cleanup)
    from . import HEALTH_PATH

    app.router.add_get(HEALTH_PATH, _handle_health)
    app.router.add_route("*", "/{tail:.*}", _handle_request)
    return app


