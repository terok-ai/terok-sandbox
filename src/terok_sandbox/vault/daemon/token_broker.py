class _RouteTable:
    """Provider routing table loaded from a JSON file."""

    def __init__(self, routes_path: str) -> None:
        with open(routes_path) as f:
            self._routes: dict[str, dict] = json.load(f)
        for name, cfg in self._routes.items():
            if "upstream" not in cfg:
                raise ValueError(f"Route '{name}' missing required 'upstream' field")
            path_upstreams = cfg.get("path_upstreams", {})
            if not isinstance(path_upstreams, dict):
                raise ValueError(f"Route '{name}' has invalid 'path_upstreams' field")
            for prefix, upstream in path_upstreams.items():
                if not isinstance(prefix, str) or not prefix.startswith("/"):
                    raise ValueError(
                        f"Route '{name}' path_upstreams key must start with '/': {prefix!r}"
                    )
                if not isinstance(upstream, str) or not upstream:
                    raise ValueError(
                        f"Route '{name}' path_upstreams entry for {prefix!r} is invalid"
                    )
            oauth_extra_headers = cfg.get("oauth_extra_headers", {})
            if not isinstance(oauth_extra_headers, dict):
                raise ValueError(f"Route '{name}' has invalid 'oauth_extra_headers' field")
            for header, value in oauth_extra_headers.items():
                if not isinstance(header, str) or not header:
                    raise ValueError(f"Route '{name}' oauth_extra_headers key is invalid")
                if not isinstance(value, str):
                    raise ValueError(
                        f"Route '{name}' oauth_extra_headers value for {header!r} is invalid"
                    )

    def get(self, provider: str) -> dict | None:
        """Return the route config for *provider*, or ``None``."""
        return self._routes.get(provider)


class _TokenDB:
    """Read-only sqlite3 accessor for phantom tokens, credentials, and SSH keys.

    Kept deliberately thin — only the SELECTs the vault daemon needs at
    request-handling time.  Writers go through
    [`terok_sandbox.vault.store.db.CredentialDB`][terok_sandbox.vault.store.db.CredentialDB].
    """

    def __init__(self, db_path: str, *, passphrase: str | None = None) -> None:
        """Open *db_path*; resolve the vault passphrase unless one is supplied."""
        # check_same_thread=False is safe: this is a read-only accessor and
        # aiohttp runs handlers in a single event loop thread.
        from ..store.encryption import open_sqlcipher

        if passphrase is None:
            from terok_sandbox.config import SandboxConfig

            from ..store.encryption import NoPassphraseError

            cfg = SandboxConfig()
            passphrase, source = cfg.resolve_passphrase_with_source()
            if passphrase is None:
                _logger.info("Vault passphrase: no tier resolved")
                raise NoPassphraseError(f"no SQLCipher passphrase available for {db_path}")
            _logger.info("Vault passphrase resolved via %s tier", source)
        self._conn = open_sqlcipher(db_path, passphrase, check_same_thread=False)
        self._conn.execute("PRAGMA journal_mode=WAL")
        # Bootstrap + migrate schema here too so the vault daemon can open
        # an empty DB on a fresh install (no CLI write has touched the file
        # yet) and a post-upgrade DB (v0 columns) without crashing on the
        # first query.  Both helpers are idempotent.
        from ..store.db import ensure_credentials_schema, migrate_credential_db_schema

        ensure_credentials_schema(self._conn)
        migrate_credential_db_schema(self._conn)

    def lookup_token(self, token: str) -> dict | None:
        """Return ``{scope, subject, credential_set, provider}`` or ``None``."""
        row = self._conn.execute(
            "SELECT scope, subject, credential_set, provider FROM proxy_tokens WHERE token = ?",
            (token,),
        ).fetchone()
        if row is None:
            return None
        return {
            "scope": row[0],
            "subject": row[1],
            "credential_set": row[2],
            "provider": row[3],
        }

    def load_credential(self, credential_set: str, provider: str) -> dict | None:
        """Return parsed credential data dict, or ``None``."""
        row = self._conn.execute(
            "SELECT data FROM credentials WHERE credential_set = ? AND provider = ?",
            (credential_set, provider),
        ).fetchone()
        return json.loads(row[0]) if row else None

    def load_ssh_keys_for_scope(self, scope: str) -> list[SSHKeyRecord]:
        """Return full SSH key records (including raw bytes) assigned to *scope*."""
        from ..store.db import SSHKeyRecord

        rows = self._conn.execute(
            "SELECT k.id, k.key_type, k.private_der, k.public_blob,"
            " k.comment, k.fingerprint"
            " FROM ssh_keys k"
            " JOIN ssh_key_assignments a ON a.key_id = k.id"
            " WHERE a.scope = ?"
            " ORDER BY a.assigned_at",
            (scope,),
        ).fetchall()
        return [SSHKeyRecord(*r) for r in rows]

    def list_refreshable(self) -> list[tuple[str, str, dict]]:
        """Return ``(credential_set, provider, data)`` for OAuth creds with a refresh token."""
        rows = self._conn.execute(
            "SELECT credential_set, provider, data FROM credentials"
        ).fetchall()
        result: list[tuple[str, str, dict]] = []
        for cs, prov, raw in rows:
            data = json.loads(raw)
            if data.get("type") == "oauth" and data.get("refresh_token"):
                result.append((cs, prov, data))
        return result

    def update_credential(self, credential_set: str, provider: str, data: dict) -> None:
        """Update the data blob for an existing credential."""
        self._conn.execute(
            "UPDATE credentials SET data = ? WHERE credential_set = ? AND provider = ?",
            (json.dumps(data), credential_set, provider),
        )
        self._conn.commit()

    def close(self) -> None:
        """Close the database connection."""
        self._conn.close()

    def __del__(self) -> None:
        """Best-effort close on garbage collection."""
        try:
            self._conn.close()
        except Exception:  # noqa: BLE001  # nosec B110 — best-effort __del__ close on GC
            pass


