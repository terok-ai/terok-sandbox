# Background OAuth token refresh
# ---------------------------------------------------------------------------


async def _do_oauth_refresh(
    session: ClientSession, provider: str, oauth_cfg: dict, cred: dict
) -> dict:
    """Call the provider's token endpoint and return an updated credential dict.

    Raises on network or HTTP errors so callers can log and continue.
    """
    payload = {
        "grant_type": "refresh_token",
        "refresh_token": cred["refresh_token"],
        "client_id": oauth_cfg["client_id"],
    }
    if "scope" in oauth_cfg:
        payload["scope"] = oauth_cfg["scope"]
    if "client_secret" in oauth_cfg:
        payload["client_secret"] = oauth_cfg["client_secret"]

    # NOTE: RFC 6749 specifies application/x-www-form-urlencoded for token
    # requests, but both Anthropic's and Codex/OpenAI's current refresh
    # endpoints accept JSON payloads.  If a future provider needs form
    # encoding, add a per-provider `token_content_type` YAML field then.
    async with session.post(
        oauth_cfg["token_url"],
        json=payload,
        timeout=ClientTimeout(total=15),
    ) as resp:
        if resp.status != 200:
            # Do NOT include the response body — token endpoints sometimes
            # echo the request payload (including the refresh_token and
            # client_secret) in error responses, and this exception is
            # logged by ``_refresh_all`` via ``_logger.exception``.
            raise RuntimeError(f"Token refresh failed (status={resp.status})")
        data = await resp.json()

    return {
        **cred,
        "access_token": data["access_token"],
        "refresh_token": data.get("refresh_token", cred["refresh_token"]),
        "expires_at": time.time() + data.get("expires_in", 28800),
    }


def _ambient_lock_dir() -> Path:
    """Refresh-lock dir derived from the ambient runtime env.

    Used as the default for standalone callers (``_build_app`` without a
    surrounding [`VaultProxy`][terok_sandbox.vault.daemon.token_broker.VaultProxy]).
    The supervisor path injects an explicit ``runtime_dir`` instead and
    pins the resolved dir on the app under ``_KEY_LOCK_DIR``.
    """
    import os as _os

    runtime = _os.environ.get("XDG_RUNTIME_DIR") or f"/run/user/{_os.getuid()}"
    return Path(runtime) / "terok" / "vault" / "locks"


async def _refresh_all(app: web.Application) -> None:
    """Check all OAuth credentials and refresh any that are expired or expiring soon.

    Cross-supervisor coordination: each per-supervisor refresh task
    contends a non-blocking ``flock`` on
    ``refresh-<credential_set>-<provider>.lock``.  A contended lock
    means another supervisor is already refreshing the same row; this
    one skips and picks up the freshly-written state on its next read.
    """
    token_db: _TokenDB = app[_KEY_TOKEN_DB]
    routes: _RouteTable = app[_KEY_ROUTES]
    session: ClientSession = app[_KEY_CLIENT]
    # The lock dir is resolved once at app-build / supervisor-start time
    # and stashed on the app — never recomputed from ambient env per
    # tick, which would misroute the path under crun's rootless userns.
    lock_dir = app[_KEY_LOCK_DIR]

    for cs, provider, cred in token_db.list_refreshable():
        route = routes.get(provider)
        if not route or "oauth_refresh" not in route:
            continue
        expires_at = cred.get("expires_at")
        if expires_at and time.time() < expires_at - _REFRESH_BUFFER:
            continue  # still valid

        fd = acquire_refresh_lock(lock_dir, cs, provider)
        if fd is None:
            _logger.debug(
                "Another supervisor is refreshing %s/%s; skipping this tick", cs, provider
            )
            continue
        try:
            # Re-read the row inside the lock — the other supervisor may
            # have refreshed it while we were waiting on dependencies.
            fresh = token_db.load_credential(cs, provider) or cred
            fresh_expires = fresh.get("expires_at")
            if fresh_expires and time.time() < fresh_expires - _REFRESH_BUFFER:
                continue  # peer already refreshed

            _logger.info("Refreshing OAuth token for %s (credential_set=%s)", provider, cs)
            try:
                new_cred = await _do_oauth_refresh(session, provider, route["oauth_refresh"], fresh)
                token_db.update_credential(cs, provider, new_cred)
                _logger.info(
                    "Refreshed OAuth token for %s (expires in %ds)",
                    provider,
                    int(new_cred["expires_at"] - time.time()),
                )
            except Exception:
                _logger.exception("Failed to refresh OAuth token for %s", provider)
        finally:
            release_refresh_lock(fd)


async def _refresh_loop(app: web.Application) -> None:
    """Run an immediate refresh check, then repeat every ``_REFRESH_INTERVAL`` seconds."""
    while True:
        try:
            await _refresh_all(app)
        except asyncio.CancelledError:
            raise
        except Exception:
            _logger.exception("OAuth refresh cycle failed")
        await asyncio.sleep(_REFRESH_INTERVAL)


