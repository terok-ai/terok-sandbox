# Periodic OAuth refresh: the supervisor wakes every
# ``_REFRESH_INTERVAL`` seconds, walks the refreshable credentials in
# scope, and renews anything expiring within ``_REFRESH_BUFFER``.  The
# buffer is generous relative to the interval so a brief scheduler
# hiccup can't drop a still-valid token to a 401.
_REFRESH_INTERVAL = 300  # seconds between background refresh checks
_REFRESH_BUFFER = 600  # refresh tokens expiring within this many seconds

# Typed app keys (avoids NotAppKeyWarning on aiohttp >= 3.9).
# ``_RouteTable`` / ``_TokenDB`` AppKeys are declared after their class
# bodies further down the file because ``AppKey`` evaluates its second
# argument at runtime and can't accept a forward-reference string.
_KEY_CLIENT = web.AppKey("client_session", ClientSession)
_KEY_REFRESH_TASK: web.AppKey[asyncio.Task[None]] = web.AppKey("refresh_task", asyncio.Task)
_KEY_AUDIT = web.AppKey("audit", AuditWriter)

_IDEMPOTENT_METHODS = frozenset({"GET", "HEAD", "OPTIONS"})
_WEBSOCKET_MAX_MSG_SIZE = 16 * 1024 * 1024
_WEBSOCKET_INTERNAL_ERROR = 1011

#: Idle read timeout (seconds) for a proxied upstream response — the gap between
#: reads (for a stream, between chunks; for a unary response, the wait for the
#: whole body to land).  Set generously so the proxy is never stricter than the
#: agent it fronts: each client enforces its own (shorter) idle timeout and gives
#: up first, so this only bounds a silently-hung upstream the client isn't
#: watching.  The 10s connect timeout still detects dead-on-arrival upstreams.
_UPSTREAM_READ_TIMEOUT = 1200

#: Max proxied request body (bytes).  aiohttp's default is 1 MiB; codex's remote
#: compaction uploads the whole transcript and exceeded it (an observed 413), and
#: large contexts or base64 images can too.  The real limit belongs to the
#: upstream, not this proxy — this is just a generous bound that keeps a single
#: buffered body from being unbounded.
_MAX_REQUEST_BODY_SIZE = 128 * 1024 * 1024

