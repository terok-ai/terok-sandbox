from dataclasses import dataclass  # noqa: E402 — keep close to use sites below


@dataclass(frozen=True)
class UnixBind:
    """Unix-domain bind for the vault HTTP proxy.

    Used by the supervisor in socket mode.  The proxy binds the socket
    with mode ``0600`` so only the same-UID rootless container peers
    via the bind-mount.
    """

    socket_path: Path


@dataclass(frozen=True)
class TcpBind:
    """TCP bind for the vault HTTP proxy.

    Used by the supervisor in TCP mode (krun / SELinux-restricted
    distros).  Always pins to ``127.0.0.1``; the shield egress
    firewall would refuse loopback traffic on any other interface
    anyway.
    """

    host: str
    port: int


class VaultProxy:
    """Embeddable aiohttp vault — one per container, owned by the supervisor.

    Each per-container supervisor builds a fresh
    [`VaultProxy`][terok_sandbox.vault.daemon.token_broker.VaultProxy]
    bound to a per-container socket (or TCP port); it serves only its
    own container and lives only as long as that container itself.

    Two transport flavours, picked by *bind* at construction:

    - [`UnixBind`][terok_sandbox.vault.daemon.token_broker.UnixBind] —
      a single mode-0600 Unix socket the rootless container reaches via
      bind-mount.  The supervisor places the socket under
      ``$XDG_RUNTIME_DIR/terok/vault/<container_id>.sock``.
    - [`TcpBind`][terok_sandbox.vault.daemon.token_broker.TcpBind] —
      ``127.0.0.1:<port>`` for the krun / SELinux-restricted path; the
      shield's loopback allowlist covers the container side.

    The OAuth refresh task runs every ``_REFRESH_INTERVAL``
    seconds inside the same loop.  Cross-supervisor coordination uses a
    non-blocking ``flock`` on
    ``$XDG_RUNTIME_DIR/terok/vault/locks/refresh-<credential_set>-<provider>.lock``:
    a contended lock means another supervisor is already refreshing the
    same row, so we skip and pick up the fresh value on the next read.
    """

    def __init__(
        self,
        *,
        db_path: Path | str,
        scope_id: str | None,
        bind: UnixBind | TcpBind,
        routes_path: Path | str | None = None,
        audit_path: Path | None = None,
        runtime_dir: Path | None = None,
    ) -> None:
        """Wire the proxy to the credential DB and a transport.

        *scope_id* is informational at present — the broker authorises
        per-token via ``_TokenDB.lookup_token``,
        not by scope.  Reserved for future per-supervisor scope-filtering
        (e.g. refusing tokens outside the supervisor's container scope).

        *routes_path* defaults to ``$XDG_CONFIG_HOME/terok/vault/routes.json``
        when omitted — the canonical location ``terok-executor`` writes
        the route table to.

        *runtime_dir* — explicit runtime root for the cross-supervisor
        refresh ``flock`` files.  Required when the proxy runs in
        crun's rootless userns (``getuid()`` reports ``0`` there but
        the actual host runtime dir is under the operator's uid); if
        omitted, falls back to ``$XDG_RUNTIME_DIR`` / ``/run/user/<uid>``.
        """
        from terok_sandbox.config import SandboxConfig

        cfg = SandboxConfig()
        self._db_path = str(db_path)
        self._scope_id = scope_id
        self._bind = bind
        self._routes_path = str(routes_path) if routes_path else str(cfg.routes_path)
        self._audit_path = audit_path
        self._runtime_dir = runtime_dir
        self._app: web.Application | None = None
        self._runner: Any | None = None
        self._site: Any | None = None
        self._refresh_lock_dir: Path | None = None

    @property
    def bind(self) -> UnixBind | TcpBind:
        """Return the transport binding the proxy was constructed with."""
        return self._bind

    @property
    def scope_id(self) -> str | None:
        """Return the scope the proxy was constructed with (informational)."""
        return self._scope_id

    async def start(self) -> None:
        """Build the aiohttp app and bring the listener up.

        Initial-pass refresh runs at startup (same shape as
        ``_refresh_loop``)
        so the first request never blocks on a slow token-endpoint
        round-trip — the periodic task continues from there.
        """
        from aiohttp.web_runner import AppRunner, SockSite, TCPSite

        self._app = _build_app(self._db_path, self._routes_path, audit_path=self._audit_path)
        self._refresh_lock_dir = self._compute_refresh_lock_dir()
        # The refresh loop reads the lock dir off the app, so pin the
        # one resolved here (from the injected runtime_dir) over the
        # ambient-env default _build_app seeded.
        self._app[_KEY_LOCK_DIR] = self._refresh_lock_dir

        self._runner = AppRunner(self._app, access_log=_logger)
        await self._runner.setup()

        if isinstance(self._bind, UnixBind):
            sock = self._bind_unix_socket(self._bind.socket_path)
            self._site = SockSite(self._runner, sock)
        else:
            self._site = TCPSite(self._runner, self._bind.host, self._bind.port)
        await self._site.start()
        _logger.info("VaultProxy started on %s", self._describe_bind())

    async def stop(self) -> None:
        """Tear down the listener and free the aiohttp resources.

        Idempotent — a stop call on a never-started proxy is a no-op.
        """
        if self._runner is not None:
            await self._runner.cleanup()
            self._runner = None
            self._site = None
            self._app = None

    def _describe_bind(self) -> str:
        """Return a human-readable description of the active bind."""
        if isinstance(self._bind, UnixBind):
            return f"unix:{self._bind.socket_path}"
        return f"tcp://{self._bind.host}:{self._bind.port}"

    def _bind_unix_socket(self, path: Path) -> socket.socket:
        """Bind a mode-0600 ``AF_UNIX`` socket at *path*, with SELinux labelling.

        Hardened sequence: a stale-but-live check rejects collisions,
        a stale-and-dead socket is unlinked, the bind happens inside
        the SELinux context manager so the ``terok_socket_t`` label is
        process-scoped, and a post-bind ``harden_socket`` re-confirms
        the inode permissions.
        """
        import socket as _socket
        import stat as _stat

        from terok_sandbox._util._net import harden_socket
        from terok_sandbox._util._selinux import socket_selinux_context

        try:
            mode = path.lstat().st_mode
        except FileNotFoundError:
            pass
        else:
            if not _stat.S_ISSOCK(mode):
                raise RuntimeError(f"Refusing to remove non-socket path: {path}")
            probe = _socket.socket(_socket.AF_UNIX, _socket.SOCK_STREAM)
            try:
                if probe.connect_ex(str(path)) == 0:
                    raise RuntimeError(f"Socket path already in use: {path}")
            finally:
                probe.close()
            path.unlink()
        path.parent.mkdir(parents=True, exist_ok=True)
        with socket_selinux_context():
            sock = _socket.socket(_socket.AF_UNIX, _socket.SOCK_STREAM)
            try:
                sock.bind(str(path))
                sock.listen(128)
            except Exception:
                sock.close()
                raise
        harden_socket(path)
        return sock

    def _compute_refresh_lock_dir(self) -> Path:
        """Pick the directory where cross-supervisor refresh ``flock`` files live.

        Uses the explicit ``runtime_dir`` passed at construction time
        when available — that's the only reliable anchor when the
        proxy runs in crun's rootless userns, where ``getuid()`` lies
        and ``XDG_RUNTIME_DIR`` may resolve to ``/run/user/0``.  Falls
        back to the ambient runtime dir for standalone callers.
        Created on demand inside ``_acquire_refresh_lock`` so a
        soft-fail mkdir doesn't crash the listener bring-up.
        """
        if self._runtime_dir is not None:
            return self._runtime_dir / "terok" / "vault" / "locks"
        return _ambient_lock_dir()


# ---------------------------------------------------------------------------
# Cross-supervisor refresh-coordination flock helper.
# ---------------------------------------------------------------------------


def _safe_lock_component(value: str) -> str:
    """Reduce *value* to a single filesystem-safe path component.

    ``credential_set`` / ``provider`` originate from the credentials DB;
    a value carrying ``/`` or ``..`` would let the composed lock path
    escape ``lock_dir``.  Map every char outside ``[A-Za-z0-9._-]`` to
    ``_`` and append a short digest of the original so two distinct
    inputs can't sanitise to the same lock file.  Deterministic: the
    same input always yields the same component, which the
    cross-supervisor ``flock`` contention relies on.
    """
    import hashlib

    cleaned = "".join(ch if ch.isalnum() or ch in "._-" else "_" for ch in value)
    digest = hashlib.sha256(value.encode("utf-8")).hexdigest()[:12]
    return f"{cleaned}-{digest}"


def acquire_refresh_lock(lock_dir: Path, credential_set: str, provider: str) -> int | None:
    """Try to acquire an exclusive ``flock`` on the per-credential refresh file.

    Returns the held file-descriptor on success, ``None`` when another
    supervisor is already refreshing the same row (the lock is
    non-blocking, ``LOCK_NB``).  Callers ``os.close`` the fd to release.
    Soft-fails to ``None`` on any I/O error — the periodic task will
    pick the credential up again on the next tick.

    The lock file lives under
    ``$XDG_RUNTIME_DIR/terok/vault/locks/refresh-<credential_set>-<provider>.lock``;
    the directory is created with mode 0700 on demand.  The
    ``credential_set`` / ``provider`` components are sanitised (see
    ``_safe_lock_component``) so the lock file always stays inside
    *lock_dir*.
    """
    import fcntl
    import os as _os

    try:
        lock_dir.mkdir(parents=True, exist_ok=True)
        _os.chmod(lock_dir, 0o700)
    except OSError as exc:
        _logger.debug("refresh lock dir setup failed: %s", exc)
        return None
    safe_cs = _safe_lock_component(credential_set)
    safe_provider = _safe_lock_component(provider)
    lock_path = lock_dir / f"refresh-{safe_cs}-{safe_provider}.lock"
    try:
        fd = _os.open(lock_path, _os.O_RDWR | _os.O_CREAT | _os.O_CLOEXEC, 0o600)
    except OSError as exc:
        _logger.debug("refresh lock open failed: %s", exc)
        return None
    try:
        fcntl.flock(fd, fcntl.LOCK_EX | fcntl.LOCK_NB)
    except OSError:
        _os.close(fd)
        return None
    return fd


def release_refresh_lock(fd: int) -> None:
    """Release the ``flock`` held by *fd* (see [`acquire_refresh_lock`][terok_sandbox.vault.daemon.token_broker.acquire_refresh_lock])."""
    import os as _os

    try:
        _os.close(fd)
    except OSError as exc:
        _logger.debug("refresh lock close failed: %s", exc)
