async def _audit_request(
    request: web.Request,
    token_info: dict | None,
    status: int,
    outcome: str,
    started_at: float,
) -> None:
    """Append one credential-audit line for *request*.  Soft-fails on every error.

    ``token_info`` may be ``None`` for early-return paths (missing or
    invalid phantom) — the caller-supplied label fields are then empty.
    The audit writer's own ``write`` is the soft-fail boundary; this
    helper just shapes the entry.  WebSocket sessions are deliberately
    not audited in v1: the proxy hands off to a separate streaming loop
    whose lifetime doesn't fit a single request line.
    """
    audit = request.app.get(_KEY_AUDIT)
    if audit is None:
        return
    info = token_info or {}
    entry = {
        "ts": datetime.now(UTC).isoformat(timespec="seconds"),
        "scope": info.get("scope", ""),
        "subject": info.get("subject", ""),
        "credential_set": info.get("credential_set", ""),
        "provider": info.get("provider", ""),
        "method": request.method,
        "path": request.path,
        "status": status,
        "outcome": outcome,
        "duration_ms": int((time.monotonic() - started_at) * 1000),
    }
    await audit.write(entry)


# ---------------------------------------------------------------------------
# Response relay and redirect resolution
# ---------------------------------------------------------------------------

# Response headers relayed verbatim to the client.  The body is re-streamed,
# so aiohttp regenerates the framing headers (content-length, content-encoding);
# copying the upstream's would contradict the bytes actually sent.
_FORWARDED_RESPONSE_HEADERS = ("content-type", "transfer-encoding", "cache-control")

# A few provider endpoints (GitHub Actions logs/artifacts, release assets, repo
# tarballs) answer with a 3xx to a self-authenticating storage URL on a *different*
# host.  The container's HTTP client is pinned to this broker's socket, so it can
# neither reach that host nor follow the hop — and the broker routes by
# token-provider, so a replayed storage path would be rewritten back onto the
# provider's API host.  The broker therefore resolves such redirects itself.
_REDIRECT_STATUSES = frozenset({301, 302, 303, 307, 308})


def _is_followable_redirect(status: int, method: str) -> bool:
    """Whether a redirect response should be resolved inside the broker.

    Only idempotent methods qualify: replaying a write body across a redirect
    could duplicate a non-idempotent effect, so those redirects are relayed back
    to the client untouched instead of being followed here.
    """
    return status in _REDIRECT_STATUSES and method.upper() in _IDEMPOTENT_METHODS


async def _stream_upstream(
    request: web.Request,
    upstream: ClientResponse,
    *,
    token_info: dict,
    started_at: float,
) -> web.StreamResponse:
    """Relay an upstream response's status, selected headers, and streamed body."""
    resp = web.StreamResponse(status=upstream.status)
    for hdr in _FORWARDED_RESPONSE_HEADERS:
        if value := upstream.headers.get(hdr):
            resp.headers[hdr] = value
    await resp.prepare(request)
    async for chunk in upstream.content.iter_any():
        await resp.write(chunk)
    await resp.write_eof()
    await _audit_request(request, token_info, upstream.status, "ok", started_at)
    return resp


async def _resolve_redirect(
    request: web.Request,
    session: ClientSession,
    location: str,
    base_url: str,
    *,
    token_info: dict,
    started_at: float,
) -> web.StreamResponse:
    """Fetch a redirect target with the injected credential dropped, then stream it.

    The ``Location`` points at a self-authenticating URL on a third-party host;
    withholding the real provider token keeps it from leaking off the provider's
    own origin — the same rule curl and browsers apply on a cross-origin redirect.
    aiohttp chases any further hops, bounded by its default redirect cap.
    """
    from urllib.parse import urljoin

    target = urljoin(base_url, location)
    follow_headers = {
        name: request.headers[name] for name in ("accept", "user-agent") if name in request.headers
    }
    async with session.get(
        target,
        headers=follow_headers,
        allow_redirects=True,
        timeout=ClientTimeout(connect=10, sock_read=_UPSTREAM_READ_TIMEOUT),
    ) as final:
        return await _stream_upstream(request, final, token_info=token_info, started_at=started_at)


async def _handle_request(request: web.Request) -> web.StreamResponse:
    """Authenticate, route by token, inject credentials, and forward to upstream."""
    started_at = time.monotonic()
    routes: _RouteTable = request.app[_KEY_ROUTES]
    token_db: _TokenDB = request.app[_KEY_TOKEN_DB]

    # 1. Validate phantom token
    phantom = _extract_phantom_token(request)
    if not phantom:
        _logger.warning("%s %s -> 401 Missing authentication", request.method, request.path)
        await _audit_request(request, None, 401, "missing_token", started_at)
        return web.Response(status=401, text="Missing authentication")

    token_info = token_db.lookup_token(phantom)
    if token_info is None:
        if static_info := _static_oauth_token_info(phantom):
            token_info = static_info
        else:
            _logger.warning("%s %s -> 401 Invalid token", request.method, request.path)
            await _audit_request(request, None, 401, "invalid_token", started_at)
            return web.Response(status=401, text="Invalid token")

    # 2. Route by token's provider
    provider = token_info["provider"]
    route = routes.get(provider)
    if route is None:
        _logger.warning(
            "%s %s -> 404 No route for provider: %s", request.method, request.path, provider
        )
        await _audit_request(request, token_info, 404, "no_route", started_at)
        return web.Response(status=404, text=f"No route for provider: {provider}")
    rest = request.path

    # 3. Load real credential
    cred = token_db.load_credential(token_info["credential_set"], provider)
    if cred is None:
        _logger.warning(
            "No credential for provider %r in set %r", provider, token_info["credential_set"]
        )
        await _audit_request(request, token_info, 502, "credential_missing", started_at)
        return web.Response(status=502, text="Credential not configured for this provider")

    # Determine the real auth value and header based on credential type.
    # OAuth tokens use "Authorization: Bearer <token>"; API keys use the
    # route-configured header (e.g. "x-api-key: <key>").
    # Legacy rows without "type" are detected by the presence of "access_token".
    cred_type = cred.get("type", "")
    is_oauth = cred_type == "oauth" or (not cred_type and "access_token" in cred)
    if is_oauth:
        real_token = cred.get("access_token")
        if not real_token:
            _logger.error("Credential for %r is OAuth but missing access_token", provider)
            await _audit_request(request, token_info, 502, "credential_misconfigured", started_at)
            return web.Response(status=502, text="Credential misconfigured")
        auth_header = "Authorization"
        auth_prefix = "Bearer "
    else:
        real_token = cred.get("token") or cred.get("key")
        if not real_token:
            _logger.error("Credential for %r has no usable token field", provider)
            await _audit_request(request, token_info, 502, "credential_misconfigured", started_at)
            return web.Response(status=502, text="Credential misconfigured")
        auth_header = route.get("auth_header", "Authorization")
        auth_prefix = route.get("auth_prefix", "Bearer ")
        # "dynamic" means auto-detect — for API keys, use x-api-key
        if auth_header == "dynamic":
            auth_header = "x-api-key"
            auth_prefix = ""

    # 4. Build upstream request — strip phantom auth, inject real auth
    upstream_base = _select_upstream(route, rest)
    upstream_url = _build_upstream_url(upstream_base, rest, request.query_string)

    # Strip auth, host, and RFC 7230 hop-by-hop headers (plus any names the
    # client listed in its Connection header) before forwarding upstream.
    dropped = {t.strip().lower() for t in request.headers.get("Connection", "").split(",")}
    dropped |= _HOP_BY_HOP_HEADERS
    dropped |= set(_AUTH_HEADERS)
    dropped.add("host")
    dropped.discard("")
    headers = {k: v for k, v in request.headers.items() if k.lower() not in dropped}
    headers[auth_header] = f"{auth_prefix}{real_token}"
    if is_oauth:
        for header, value in (route.get("oauth_extra_headers") or {}).items():
            existing_values = [
                part.strip() for part in headers.get(header, "").split(",") if part.strip()
            ]
            if value not in existing_values:
                headers[header] = ",".join([*existing_values, value])

    session: ClientSession = request.app[_KEY_CLIENT]
    if request.headers.get("Upgrade", "").lower() == "websocket":
        # WebSocket sessions deliberately bypass the audit log in v1 — the
        # streaming loop has no single status / duration we can record
        # inside one JSONL line.  Per-frame audit is unbounded; an open /
        # close pair is the natural follow-up.
        return await _proxy_websocket(
            request,
            session=session,
            upstream_url=upstream_url,
            headers=headers,
            provider=provider,
        )

    # 5. Forward and stream response
    # Read the body once (streaming body can only be consumed once).
    body = await request.read() if request.can_read_body else None
    # Only retry idempotent methods (or those with an Idempotency-Key header) to
    # avoid replaying non-idempotent writes on stale-connection recovery.
    can_retry = (
        request.method.upper() in _IDEMPOTENT_METHODS or "idempotency-key" in request.headers
    )
    for attempt in range(2 if can_retry else 1):
        # Track whether __aenter__ succeeded so we never retry after resp.prepare().
        connection_established = False
        try:
            async with session.request(
                request.method,
                upstream_url,
                headers=headers,
                data=body,
                allow_redirects=False,
                timeout=ClientTimeout(connect=10, sock_read=_UPSTREAM_READ_TIMEOUT),
            ) as upstream:
                connection_established = True
                location = upstream.headers.get("location")
                if location and _is_followable_redirect(upstream.status, request.method):
                    return await _resolve_redirect(
                        request,
                        session,
                        location,
                        str(upstream.url),
                        token_info=token_info,
                        started_at=started_at,
                    )
                return await _stream_upstream(
                    request, upstream, token_info=token_info, started_at=started_at
                )
        except ServerDisconnectedError:
            if not connection_established and attempt == 0 and can_retry:
                _logger.debug("Upstream %s disconnected on pooled conn, retrying", provider)
                continue
            _logger.error("Upstream %s disconnected on fresh connection", provider)
            await _audit_request(request, token_info, 502, "upstream_disconnected", started_at)
            return web.Response(status=502, text="Upstream disconnected")
        except Exception as exc:
            _logger.error("Upstream request to %s failed: %s", provider, exc)
            await _audit_request(request, token_info, 502, "upstream_error", started_at)
            return web.Response(status=502, text="Upstream request failed")
    await _audit_request(request, token_info, 502, "upstream_disconnected", started_at)
    return web.Response(status=502, text="Upstream disconnected")  # unreachable


